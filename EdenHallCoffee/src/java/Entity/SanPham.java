package Entity;

/**
 *
 * @author Nguyen Tien Thuat
 */
public class SanPham {
    private int id;
    private String name;
    private String url;
    private int price;
    private String type;

    public SanPham() {
    }

    public SanPham(String name, String url, int price, String type) {
        this.name = name;
        this.url = url;
        this.price = price;
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    
}
